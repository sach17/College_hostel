<br />
IIT Patna’s permanent campus will be located at Bihta, 35 km from Patna at a 500 acres (200 ha) site. Permanent campus plan of IIT Patna: http://www.iitp.ac.in/plan.html

The foundation stone of the IIT Patna, Bihta Campus was laid by Sri Kapil Sibal in 2011. It is currently under construction. At present, the institute is operating from a temporary 10 acres (4.0 ha) complex in Pataliputra colony, Patna, in buildings which have been renovated and were previously used by the Naveen Government Polytechnic.
<br />
The students' gymkhana of IIT Patna(established in 2008) is the Students' Governing Council consisting of the elected student representatives working for the over-all well being and holistic development of students in the institute.

The elected representatives of the students from each hostel strive to foster growth and leadership among the students by coordinating various events helping to extract the maximum of ones talent. To promote the co-curricular activities and interests, the gymkhana has many clubs and groups dedicated to the interests of the students.

Sports at IIT Patna bring together relaxation and competition getting the best of every player. It organizes various inter-hostel and inter-department competitions.

The blending of different cultures has lent uniqueness to the cultural activities which are organized throughout the year. The organization of various workshops, seminars and various technical activities at IIT P makes learning an enjoyable experience.

All these and numerous other clubs come together to organize the big fests - ANWESHA, CELESTA, REVERBERANCE, etc... and together make the IITP legacy.<br>
Games and sports are organized through the Gymkhana. It promotes all extra curricular activities in the field of Sports. The Institute has on its rolls Chairman Sports, Coach and other supporting staff. Facilities for the following games and sports are available: Basketball, Volleyball, Tennis, Badminton, Table Tennis, Athletics, Cricket, Chess, and Carom etc. In addition to this, Student Gymkhana has well equipped gymnasium.

Besides organizing routine activities in above-mentioned sports, the Gymkhana also organizes Inter Hostel Sports Meet. The Gymkhana also takes the responsibility of preparing students for Inter-IIT Sports Meet.</div> 
